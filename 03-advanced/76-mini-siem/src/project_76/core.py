# ── Log parsers ────────────────────────────────────────────────────────────────

class LogParser:
    """Base log parser — override parse_line."""

    source_name: str = "generic"

    _TS_FORMATS = (
        "%Y-%m-%dT%H:%M:%S%z",
        "%Y-%m-%d %H:%M:%S",
        "%b %d %H:%M:%S",
    )

    def parse_line(self, line: str) -> LogEvent | None:
        """Parse a single log line into a LogEvent, or return None to skip."""
        line = line.strip()
        if not line:
            return None
        ts = datetime.now(timezone.utc)
        event_id = LogEvent._make_id(line, ts)
        return LogEvent(
            event_id=event_id,
            timestamp=ts,
            source=self.source_name,
            message=line,
            raw=line,
        )


class SyslogParser(LogParser):
    """Parser for /var/log/syslog and /var/log/auth.log."""

    source_name = "syslog"
    _PATTERN = re.compile(
        r"^(?P<month>\w+)\s+(?P<day>\d+)\s+(?P<time>\d{2}:\d{2}:\d{2})\s+"
        r"(?P<host>\S+)\s+(?P<proc>\S+):\s+(?P<msg>.+)$"
    )

    def parse_line(self, line: str) -> LogEvent | None:
        """Parse syslog-format line."""
        line = line.strip()
        if not line:
            return None
        m = self._PATTERN.match(line)
        ts = datetime.now(timezone.utc)
        fields: dict[str, str] = {}
        message = line
        if m:
            fields = m.groupdict()
            message = fields.get("msg", line)
        event_id = LogEvent._make_id(line, ts)
        return LogEvent(
            event_id=event_id,
            timestamp=ts,
            source=self.source_name,
            message=message,
            raw=line,
            fields=fields,
        )


class ApacheParser(LogParser):
    """Parser for Apache/Nginx combined access log."""

    source_name = "apache"
    _PATTERN = re.compile(
        r'^(?P<ip>\S+) \S+ \S+ \[(?P<time>[^\]]+)\] "(?P<request>[^"]*)" '
        r"(?P<status>\d{3}) (?P<size>\S+)"
    )

    def parse_line(self, line: str) -> LogEvent | None:
        """Parse combined access log line."""
        line = line.strip()
        if not line:
            return None
        m = self._PATTERN.match(line)
        ts = datetime.now(timezone.utc)
        fields: dict[str, str] = {}
        message = line
        if m:
            fields = m.groupdict()
            message = f"{fields['ip']} {fields['request']} {fields['status']}"
        event_id = LogEvent._make_id(line, ts)
        return LogEvent(
            event_id=event_id,
            timestamp=ts,
            source=self.source_name,
            message=message,
            raw=line,
            fields=fields,
        )


_PARSERS: dict[str, LogParser] = {
    "syslog": SyslogParser(),
    "apache": ApacheParser(),
    "generic": LogParser(),
}


def get_parser(name: str) -> LogParser:
    """Return parser by name, defaulting to generic."""
    return _PARSERS.get(name, LogParser())


# ── Alert store ────────────────────────────────────────────────────────────────

class AlertStore:
    """Thread-safe in-memory alert store with optional file persistence."""

    def __init__(self, output_path: Path | None = None) -> None:
        self._alerts: list[Alert] = []
        self._lock = threading.Lock()
        self._output_path = output_path

    def add(self, alert: Alert) -> None:
        """Add alert and optionally write to JSONL file."""
        with self._lock:
            self._alerts.append(alert)
            if self._output_path:
                with self._output_path.open("a") as fh:
                    fh.write(json.dumps(alert.to_dict()) + "\n")
        logger.warning("[ALERT] %s | %s | %s", alert.severity.value, alert.rule_name, alert.message)

    def get_all(self) -> list[Alert]:
        """Return a snapshot of all alerts."""
        with self._lock:
            return list(self._alerts)

    def count_by_severity(self) -> dict[str, int]:
        """Return counts grouped by severity."""
        counts: dict[str, int] = {s.value: 0 for s in Severity}
        with self._lock:
            for a in self._alerts:
                counts[a.severity.value] += 1
        return counts


# ── SIEM engine ────────────────────────────────────────────────────────────────

class SIEMEngine:
    """Correlates log events against detection rules and emits alerts."""

    def __init__(
        self,
        rules: list[DetectionRule] | None = None,
        alert_store: AlertStore | None = None,
        alert_callbacks: list[Callable[[Alert], None]] | None = None,
    ) -> None:
        self._rules = rules if rules is not None else list(BUILTIN_RULES)
        self._store = alert_store or AlertStore()
        self._callbacks = alert_callbacks or []

    @property
    def alert_store(self) -> AlertStore:
        """Expose the alert store."""
        return self._store

    def process_event(self, event: LogEvent) -> list[Alert]:
        """Run all rules against event; store and return any alerts fired."""
        fired: list[Alert] = []
        for rule in self._rules:
            if rule.matches(event):
                alert_id = hashlib.sha256(
                    f"{rule.name}{event.event_id}".encode()
                ).hexdigest()[:16]
                alert = Alert(
                    alert_id=alert_id,
                    rule_name=rule.name,
                    severity=rule.severity,
                    message=rule.description,
                    event=event,
                )
                self._store.add(alert)
                for cb in self._callbacks:
                    cb(alert)
                fired.append(alert)
        return fired

    def ingest_file(self, log_path: Path, parser: LogParser) -> tuple[int, int]:
        """Ingest a log file; return (lines_processed, alerts_fired)."""
        lines = events = alerts = 0
        with log_path.open(errors="replace") as fh:
            for raw_line in fh:
                lines += 1
                event = parser.parse_line(raw_line)
                if event:
                    events += 1
                    alerts += len(self.process_event(event))
        logger.info("Ingested %s: %d lines → %d events → %d alerts", log_path.name, lines, events, alerts)
        return lines, alerts

    def tail_file(self, log_path: Path, parser: LogParser, stop_event: threading.Event) -> None:
        """Tail a log file in real-time until stop_event is set."""
        with log_path.open(errors="replace") as fh:
            fh.seek(0, 2)  # seek to end
            while not stop_event.is_set():
                line = fh.readline()
                if line:
                    event = parser.parse_line(line)
                    if event:
                        self.process_event(event)
                else:
                    time.sleep(0.1)
