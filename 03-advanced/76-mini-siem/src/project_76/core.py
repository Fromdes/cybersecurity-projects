from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import Any, Callable

logger = logging.getLogger(__name__)

# ── Severity ──────────────────────────────────────────────────────────────────

class Severity(str, Enum):
    """Alert severity levels."""

    LOW = "LOW"
    MEDIUM = "MEDIUM"
    HIGH = "HIGH"
    CRITICAL = "CRITICAL"


# ── Normalized log event ───────────────────────────────────────────────────────

@dataclass(frozen=True)
class LogEvent:
    """Normalized log event fed into the detection engine."""

    event_id: str
    timestamp: datetime
    source: str
    message: str
    raw: str
    fields: dict[str, str] = field(default_factory=dict)

    @staticmethod
    def _make_id(raw: str, ts: datetime) -> str:
        digest = hashlib.sha256(f"{ts.isoformat()}{raw}".encode()).hexdigest()[:16]
        return digest


@dataclass(frozen=True)
class Alert:
    """SIEM alert generated by a detection rule."""

    alert_id: str
    rule_name: str
    severity: Severity
    message: str
    event: LogEvent
    triggered_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))

    def to_dict(self) -> dict[str, Any]:
        """Serialize to dict for JSON output."""
        return {
            "alert_id": self.alert_id,
            "rule_name": self.rule_name,
            "severity": self.severity.value,
            "message": self.message,
            "triggered_at": self.triggered_at.isoformat(),
            "event": {
                "event_id": self.event.event_id,
                "timestamp": self.event.timestamp.isoformat(),
                "source": self.event.source,
                "message": self.event.message,
            },
        }


# ── Detection rules ────────────────────────────────────────────────────────────

@dataclass
class DetectionRule:
    """Regex-based detection rule."""

    name: str
    pattern: re.Pattern[str]
    severity: Severity
    description: str

    def matches(self, event: LogEvent) -> bool:
        """Return True if the rule fires on this event."""
        return bool(self.pattern.search(event.message) or self.pattern.search(event.raw))


